    private void showOnTable(List<ViewEmpsel> list) {
         // 1. 获取表格(tblProduct)模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblEmp.getModel();
        // 2. 清空表格信息
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        // 3.显示数据
        for (ViewEmpsel v : list) {
            Vector vt = new Vector();
            vt.add(v.getEmpid());
            vt.add(v.getEname());
            vt.add(v.getTotalcount());
            vt.add(v.getTotalprice());
            dtm.addRow(vt);
        }
    }

