    private void showOnTable(List<ViewPurchase> list) {
        //1 表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblPurchase.getModel();
        //2清空表格
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        //3 添加数据
        for (ViewPurchase pur : list) {
            Vector vt = new Vector();
            vt.add(pur.getPurid());
            vt.add(pur.getProid());
            vt.add(pur.getProname());
            vt.add(pur.getType());
            vt.add(pur.getNowcount());
            vt.add(pur.getSupname());
            vt.add(pur.getPurcount());
            vt.add(pur.getPurprice());
            vt.add(pur.getPurdate());
            dtm.addRow(vt);

        }

    }

