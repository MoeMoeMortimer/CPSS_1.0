    private void btnQueryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQueryActionPerformed
        //按条件查询采购单
        //1获取条件
        String start = this.dateStart.getText();
        String end = this.dateEnd.getText();
        String conditions = this.txtCondition.getText().trim();
        //调用业务
        List<ViewPurchase> list = pbiz.findByCondition(start, end, conditions);
        //显示在表格中
        showOnTable(list);

    }//GEN-LAST:event_btnQueryActionPerformed

    private void tblPurchaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPurchaseMouseClicked
        selectrow = this.tblPurchase.getSelectedRow();//selectrow为类属性
        Integer purid = (Integer) this.tblPurchase.getValueAt(selectrow, 0);
        Integer pid = (Integer) this.tblPurchase.getValueAt(selectrow, 1);
        Integer purchaseamount = (Integer) this.tblPurchase.getValueAt(selectrow, 6);
        //显示在文本框
        this.txtPurid.setText(purid.toString());
        this.txtProid.setText(pid.toString());
        this.txtPurcount.setText(purchaseamount.toString());

    }//GEN-LAST:event_tblPurchaseMouseClicked

    private void btnPurReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurReturnActionPerformed
        // TODO add your handling code here:
        if (selectrow == -1) {
            JOptionPane.showMessageDialog(this, "请选择要退货的采购单信息");
            return;
        }

        //获取退货信息
        Integer purid = Integer.parseInt(this.txtPurid.getText());
        Integer proid = Integer.parseInt(this.txtProid.getText());
        Integer purcount = Integer.parseInt(this.txtPurcount.getText());
        Supplier sup = supbiz.findBySubQuery(purid);
        int supid = sup.getSupid();
        BigDecimal prtprice = new BigDecimal(this.tblPurchase.getValueAt(selectrow, 7).toString());
        //获取退货数量
        String sreturnAmount = this.txtReturnAmount.getText();
        //数据验证
        String regex = "[1-9][0-9]*";
        if (sreturnAmount.matches(regex) == false) {
            JOptionPane.showMessageDialog(this, "请填写整数");
            return;
        }
        // 转换退货数量
        Integer prtcount = Integer.parseInt(sreturnAmount);
        // 判断是否超过采购数量
        if (prtcount > purcount) {
            JOptionPane.showMessageDialog(this, "退货数量大于采购数量");
            return;
        }
        // 获取退货总金额
       // BigDecimal prttotal = new BigDecimal(prtprice.multiply(new BigDecimal(prtcount)).toString());
        // 调用业务
        java.sql.Date prtdate = new java.sql.Date(new java.util.Date().getTime());

        boolean result = pbiz.purchasereturn(proid, purid, supid, prtcount, prtprice, prtdate);
        if (result == true) {
            JOptionPane.showMessageDialog(this, "退货成功");
            btnQueryActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(this, "退货失败");
        }
        //清空文本框
        this.txtProid.setText("");
        this.txtPurid.setText("");
        this.txtPurcount.setText("");
        this.txtReturnAmount.setText("");
        selectrow = -1;
    }//GEN-LAST:event_btnPurReturnActionPerformed

