    private void showOnPurchaseTable(List<ViewPurchase> list) {
         // 1. 获取表格(tblProduct)模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblPurchase.getModel();
        // 2. 清空表格信息
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        // 3.显示数据
//        	private int purid;
//	private int proid;
//	private String proname;
//        private int empid;
//	private String ename;
//	private int supid;
//	private String supname;
//	private String purdate;
//	private int purcount;
//	private BigDecimal purprice;
//	private BigDecimal purtotal;
        for (ViewPurchase p : list) {
            Vector vt = new Vector();
            vt.add(p.getPurid());
            vt.add(p.getProname());
            vt.add(p.getType());
            vt.add(p.getSupname());
            vt.add(p.getPurprice());
            vt.add(p.getPurcount());
            vt.add(p.getPurprice().multiply(new BigDecimal(p.getPurcount())));
            vt.add(p.getPurdate());
            
            dtm.addRow(vt);
        }

    }
