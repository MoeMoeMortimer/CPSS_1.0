    private void addPurchaseTable(Vector vt) {
        boolean flag = false;
        DefaultTableModel dtm = (DefaultTableModel) this.tblPurchase.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            if (vt.get(0) == tblPurchase.getValueAt(i, 0)) {
                flag = true;
                break;
            }
        }
        if (flag == false) {
            dtm.addRow(vt);
        }
    }

    private void showOnProductTable(List<Product> list) {
        // 1. 获取表格(tblProduct)模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblProduct.getModel();
        // 2. 清空表格信息
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        // 3.显示数据
        for (Product p : list) {
            Vector vt = new Vector();
            vt.add(p.getProid());
            vt.add(p.getProname());
            vt.add(p.getType());
            vt.add(p.getColor());
            vt.add(p.getSugpurchase());
            vt.add(p.getSafecount());
            vt.add(p.getNowcount());
            dtm.addRow(vt);
        }

    }

    public void initPurchaseTable() {
        JComboBox cobsup = new JComboBox();
        List<Supplier> suplist = supbiz.findAll();
        for (Supplier sup : suplist) {
            cobsup.addItem(sup);
        }
        this.tblPurchase.getColumnModel().getColumn(3).setCellEditor(new DefaultCellEditor(cobsup));
    }

