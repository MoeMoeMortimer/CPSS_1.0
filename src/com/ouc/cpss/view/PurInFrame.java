    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "请选择要删除的采购信息");
            return;
        }
        //tblPurchase :采购信息表变量名
        DefaultTableModel dtm = (DefaultTableModel) this.tblPurchase.getModel();
        dtm.removeRow(selectedRow);
        selectedRow = -1; //还原选择行号

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnPurInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurInActionPerformed
        //加入采购 按钮                                      
        //获取信息
        DefaultTableModel dtm = (DefaultTableModel) this.tblPurchase.getModel();
        //行根据表格中的数据行，列根据sql语句中的参数
        Object[][] purchase = new Object[dtm.getRowCount()][6];//根据具体数值数创建采购数组
        Object[][] stocks = new Object[dtm.getRowCount()][2];//商品库存数组
        //遍历表格
        for (int i = 0; i < dtm.getRowCount(); i++) {
            //采购表
            //purid,proid,supid,purdate,purcount,purprice,purtotal,state) values(?,?,?,?,?,?,?,1)
            //getValueAt(i, 4)中的4指窗体采购表中的第4列
            purchase[i][0] = null;
            purchase[i][1] = this.tblPurchase.getValueAt(i, 0);//商品编号
            Supplier sup = (Supplier) this.tblPurchase.getValueAt(i, 3);
            purchase[i][2] = sup.getSupid();//供应商id
            purchase[i][3] = new java.util.Date();

            String count_s = (String) this.tblPurchase.getValueAt(i, 4);
            if (StringUtil.checkDigit(count_s)) {
                purchase[i][4] = new Integer(count_s); //采购数量
            } else {
                JOptionPane.showMessageDialog(this, "数量必须为数字！");
                return;
            }

            //采购价格（验证）
            String price_s = (String) this.tblPurchase.getValueAt(i, 5);
            if (StringUtil.checkDecimal(price_s)) {
                purchase[i][5] = new BigDecimal(price_s);//采购价格
            } else {
                JOptionPane.showMessageDialog(this, "价格必须为数字！");
                return;
            }

            //总金额，价格 * 数量
            //purchase[i][6] = new BigDecimal(purchase[i][4].toString()).multiply(new BigDecimal(purchase[i][5].toString()));
            this.tblPurchase.setValueAt(new BigDecimal(purchase[i][4].toString()).multiply(new BigDecimal(purchase[i][5].toString())), i, 6);

            stocks[i][0] = this.tblPurchase.getValueAt(i, 4);  //采购数量
            stocks[i][1] = this.tblPurchase.getValueAt(i, 0); //商品编号

        }
        //调用批量添加
        boolean result = purbiz.purchasein(purchase, stocks);
        if (result == true) {
            JOptionPane.showMessageDialog(this, "采购入库成功");
            //更新商品表
            btnSearchActionPerformed(evt);//调用查询方法
        } else {
            JOptionPane.showMessageDialog(this, "采购入库失败");
        }
        //清空采购表
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
    }//GEN-LAST:event_btnPurInActionPerformed

